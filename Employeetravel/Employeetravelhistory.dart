  Future<String?> getAuthToken() async {
    SharedPreferences prefs = await SharedPreferences.getInstance();
    return prefs.getString('token');
  }

  Future<void> _fetchEmployeeTravel() async {
    final token = await getAuthToken();
    final String? jwt3 = token;
    const String apiUrl = '$apiBaseUrl/hr/api/employee-travel-get';

    final response = await http.get(
      Uri.parse(apiUrl),
      headers: {
        'Content-Type': 'application/json',
        'Authorization': 'Bearer $jwt3',
      },
    );

    if (response.statusCode == 200) {
      setState(() {
        _employeeTravel = json.decode(response.body);
      });
    } else {
      // Handle error
      print('Failed to fetch Document Visa: ${response.statusCode}');
    }
  }

  Future<void> _deleteEmployeeTravel(int travelId) async {
    final token = await getAuthToken();
    final String? jwt3 = token;
    final String apiUrl = '$apiBaseUrl/hr/api/delete-employee-travel/$travelId';

    final response = await http.delete(
      Uri.parse(apiUrl),
      headers: {
        'Content-Type': 'application/json',
        'Authorization': 'Bearer $jwt3',
      },
    );

    if (response.statusCode == 200) {
      setState(() {
        _employeeTravel.removeWhere((visa) => visa['id'] == travelId);
      });
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text(' visa deleted successfully'),
        ),
      );
    } else {
      // Show appropriate error message based on response status code
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text('Failed to delete visa'),
        ),
      );
    }
  }

  Future<Map<String, dynamic>> fetchEmployeeTravelData(int travelId) async {
    final token = await getAuthToken();
  final String? jwt3 = token;
  final String apiUrl = '$apiBaseUrl/hr/api/travel-get/$travelId';

  final response = await http.get(
    Uri.parse(apiUrl),
    headers: {
      'Content-Type': 'application/json',
      'Authorization': 'Bearer $jwt3',
    },
  );

  if (response.statusCode == 200) {
    return json.decode(response.body);
  } else {
    throw Exception('Failed to fetch employee tavel data');
  }
